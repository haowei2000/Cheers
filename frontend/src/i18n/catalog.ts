export type AppLanguage = "en" | "zh-CN" | "auto-ja" | "auto-ko" | "auto-es" | "auto-fr" | "auto-de";

export type LanguageOption = {
  code: AppLanguage;
  label: string;
  nativeLabel: string;
  targetLanguage: string;
  mode: "curated" | "auto";
};

export const LANGUAGE_STORAGE_KEY = "agentnexus.language";
export const DEFAULT_LANGUAGE: AppLanguage = "en";

export const LANGUAGE_OPTIONS: LanguageOption[] = [
  { code: "en", label: "English", nativeLabel: "English", targetLanguage: "en", mode: "curated" },
  { code: "zh-CN", label: "Chinese", nativeLabel: "中文", targetLanguage: "zh-CN", mode: "curated" },
  { code: "auto-ja", label: "Japanese", nativeLabel: "日本語", targetLanguage: "ja", mode: "auto" },
  { code: "auto-ko", label: "Korean", nativeLabel: "한국어", targetLanguage: "ko", mode: "auto" },
  { code: "auto-es", label: "Spanish", nativeLabel: "Español", targetLanguage: "es", mode: "auto" },
  { code: "auto-fr", label: "French", nativeLabel: "Français", targetLanguage: "fr", mode: "auto" },
  { code: "auto-de", label: "German", nativeLabel: "Deutsch", targetLanguage: "de", mode: "auto" },
];

export const LANGUAGE_BY_CODE = new Map(LANGUAGE_OPTIONS.map((item) => [item.code, item]));

export function normalizeLanguage(value: string | null | undefined): AppLanguage {
  if (value && LANGUAGE_BY_CODE.has(value as AppLanguage)) return value as AppLanguage;
  return DEFAULT_LANGUAGE;
}

